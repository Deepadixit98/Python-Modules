# math Module
import math

# constants
print(math.pi)
print(math.e)

# triginometric functions
print(math.radians(180))
print(math.radians(30))

print(math.degrees(2 * math.pi))

print(math.sin(math.radians(180)))
print(math.sin(math.radians(30)))

print(math.cos(math.radians(30)))

print(math.tan(math.radians(30)))

# logarithemic functions

print(math.log(10))

print(math.log10(100))

print(math.exp(2))

print(math.pow(2, 3))

print(math.sqrt(225))

# representation function

print(math.ceil(12.5))

print(math.floor(12.5))

